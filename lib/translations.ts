export function navLabelFor(path: string, language: Language) {
  const t = translations[language].nav;

  switch (path) {
    case "/":
      return t.home;
    case "/solutions":
      return t.solutions;
    case "/reviews":
      return t.reviews;
    case "/service-areas":
      return t.serviceAreas;
    case "/about":
      return t.about;
    case "/blog":
      return t.resources;
    case "/contact":
      return t.freeAssessment;
    case "/battery-storage":
      return t.batteryStorage;
    case "/roofing":
      return t.roofing;
    default:
      return "";
  }
}

